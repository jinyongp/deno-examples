/**
 * Add two numbers
 * @param {number} a
 * @param {number} b
 * @returns {number} added two numbers
 */
export function add(a: number, b: number): number {
  return a + b;
}

/**
 * Multiply two numbers
 * @param {number} a
 * @param {number} b
 * @returns {number} multiplied two numbers
 */
export function multiply(a: number, b: number): number {
  return a * b;
}

